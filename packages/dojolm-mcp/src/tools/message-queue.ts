function buildResult(
  text: string,
  payloadId: string,
  isError = false,
): AdversarialToolResult {
  return {
    content: [{ type: 'text', text }],
    isError,
    metadata: {
      attackType: 'message-queue-exploitation',
      payloadId,
    },
  };
}

function pickPayloadForMode(
  payloads: readonly AttackPayload[],
  mode: AttackModeName,
): AttackPayload | undefined {
  const modeRank = MIN_MODE_RANK[mode];
  // Filter payloads enabled for the current mode, then pick highest severity
  const eligible = payloads.filter((p) => modeRank >= MIN_MODE_RANK[p.minMode]);
  if (eligible.length === 0) return undefined;
  return eligible.reduce((best, cur) =>
    (SEVERITY_RANK[cur.severity] ?? 0) > (SEVERITY_RANK[best.severity] ?? 0) ? cur : best,
  );
}

