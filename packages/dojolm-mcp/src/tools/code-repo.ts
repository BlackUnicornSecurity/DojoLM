/**
 * Build an AdversarialToolResult with code-repository-poisoning metadata.
 */
function buildRepoResult(
  text: string,
  payloadId: string,
  isError = false,
): AdversarialToolResult {
  return {
    content: [{ type: 'text', text }],
    isError,
    metadata: {
      attackType: 'code-repository-poisoning',
      payloadId,
    },
  };
}

