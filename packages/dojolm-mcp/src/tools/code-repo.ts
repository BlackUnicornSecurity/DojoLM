// ---------------------------------------------------------------------------
// Mode ranking helpers (consistent with existing codebase pattern)
// ---------------------------------------------------------------------------

const MODE_RANK: Record<AttackModeName, number> = {
  passive: 0,
  basic: 1,
  advanced: 2,
  aggressive: 3,
};

function getPayloadsForMode(
  payloads: readonly AttackPayload[],
  mode: AttackModeName,
): AttackPayload[] {
  const rank = MODE_RANK[mode];
  return payloads.filter((p) => MODE_RANK[p.minMode] <= rank);
}

function pickHighestPayload(
  payloads: readonly AttackPayload[],
  mode: AttackModeName,
): AttackPayload | undefined {
  const eligible = getPayloadsForMode(payloads, mode);
  if (eligible.length === 0) return undefined;
  const severityRank: Record<string, number> = { low: 0, medium: 1, high: 2, critical: 3 };
  return eligible.reduce((best, p) =>
    severityRank[p.severity] > severityRank[best.severity] ? p : best,
  );
}

