/**
 * Get test cases by category
 */
export function getTestCasesByCategory(category: string): LLMPromptTestCase[] {
  return SAMPLE_TEST_CASES.filter(tc => tc.category === category);
}

/**
 * Get test cases by OWASP category
 */
export function getTestCasesByOWASP(owaspCategory: string): LLMPromptTestCase[] {
  return SAMPLE_TEST_CASES.filter(tc => tc.owaspCategory === owaspCategory);
}

/**
 * Get test cases by TPI story
 */
export function getTestCasesByTPI(tpiStory: string): LLMPromptTestCase[] {
  return SAMPLE_TEST_CASES.filter(tc => tc.tpiStory === tpiStory);
}

/**
 * Get test cases by severity
 */
export function getTestCasesBySeverity(severity: string): LLMPromptTestCase[] {
  return SAMPLE_TEST_CASES.filter(tc => tc.severity === severity);
}

/**
 * Get test cases by tag
 */
export function getTestCasesByTag(tag: string): LLMPromptTestCase[] {
  return SAMPLE_TEST_CASES.filter(tc => tc.tags?.includes(tag));
}

/**
 * Get test cases by scenario
 * For S-011 (Full Scope), returns all enabled test cases
 * For other scenarios, returns test cases matching the scenario's categories
 */
export function getTestCasesByScenario(scenario: TestScenario): LLMPromptTestCase[] {
  if (isFullScopeScenario(scenario)) {
    return SAMPLE_TEST_CASES.filter(tc => tc.enabled);
  }

  const categories = getTestCasesForScenario(scenario);
  return SAMPLE_TEST_CASES.filter(tc =>
    tc.enabled && categories.includes(tc.category)
  );
}

/**
 * Get test cases by multiple scenarios
 * Useful for combining focused scenarios (e.g., S-001 + S-002)
 */
export function getTestCasesByScenarios(scenarios: TestScenario[]): LLMPromptTestCase[] {
  const allCases = scenarios.flatMap(s => getTestCasesByScenario(s));
  // Deduplicate by ID
  const seen = new Set<string>();
  return allCases.filter(tc => {
    if (seen.has(tc.id)) return false;
    seen.add(tc.id);
    return true;
  });
}

/**
 * Get test cases count by category
 */
export function getTestCasesCountByCategory(): Record<string, number> {
  const counts: Record<string, number> = {};
  for (const tc of SAMPLE_TEST_CASES) {
    counts[tc.category] = (counts[tc.category] || 0) + 1;
  }
  return counts;
}

/**
 * Seed test cases to storage
 */
export async function seedTestCases(): Promise<{ seeded: number; failed: number }> {
  const { fileStorage } = await import('../storage/file-storage');

  let seeded = 0;
  let failed = 0;

  for (const testCase of SAMPLE_TEST_CASES) {
    try {
      await fileStorage.saveTestCase(testCase);
      seeded++;
    } catch (error) {
      console.error(`Failed to seed test case ${testCase.id}:`, error);
      failed++;
    }
  }

  return { seeded, failed };
}
